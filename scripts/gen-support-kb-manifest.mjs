import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = "supabase/functions/_shared/support-kb";
const out = [];
function walk(dir) {
  for (const name of readdirSync(dir)) {
    if (name.startsWith("_") || name === "README.md") continue;
    const p = join(dir, name);
    const s = statSync(p);
    if (s.isDirectory()) walk(p);
    else if (name.endsWith(".md")) out.push(p);
  }
}
walk(ROOT);
out.sort();
const entries = out.map((p) => {
  const rel = relative(ROOT, p).replace(/\\/g, "/");
  const content = readFileSync(p, "utf8");
  return `  ${JSON.stringify(rel)}: ${JSON.stringify(content)},`;
}).join("\n");
const ts = `// AUTO-GENERATED by scripts/gen-support-kb-manifest.mjs. Do not edit by hand.
// Snapshot of supabase/functions/_shared/support-kb/**.md used by support-kb-sync.

export const SUPPORT_KB_FILES: Record<string, string> = {
${entries}
};

export const SUPPORT_KB_FILE_COUNT = ${out.length};
`;
writeFileSync("supabase/functions/_shared/support-kb-manifest.ts", ts);
console.log("wrote", out.length, "files");
